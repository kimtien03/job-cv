package org.example.Models;

public class Job {

    private Integer id;

    private String name;

    // Constructor mặc định
    public Job() {
    }

    // Getters and Setters

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
